public class VotesPercent
{
   public static void main(String[] args)
   {
      int coalitionVotes = 6908710;
      int laborVotes = 6006217;
      int formalVotes = coalitionVotes + laborVotes;
      System.out.println(formalVotes);
      double percentOfVotes = (double) laborVotes / formalVotes * 100.0;
      System.out.println(percentOfVotes);
   }
}
